// Alias: ambos lados del cuerpo comparten ejercicios
const EXERCISE_ALIASES = {
  back_forearms: 'front_forearms',
  back_calves: 'front_calves',
  front_neck: 'traps',
}

const SUPPLEMENT_EQUIPMENT = ['dumbbells', 'barbell', 'cables', 'machines', 'bodyweight', 'band', 'kettlebells', 'plates']
const MIN_PER_MUSCLE = 3
const MAX_PER_MUSCLE = 4

export function getExercisesForMusclesAndEquipment(muscles, equipment) {
  const result = []
  const seen = new Set()

  for (const muscleRaw of muscles) {
    const muscle = EXERCISE_ALIASES[muscleRaw] || muscleRaw
    const muscleData = BASE_EXERCISES[muscle]
    if (!muscleData) continue

    const muscleExercises = []

    // 1. Ejercicios que coinciden con el equipamiento seleccionado (primarios)
    for (const eq of equipment) {
      if (muscleExercises.length >= MAX_PER_MUSCLE) break
      const exList = muscleData[eq]
      if (!exList) continue
      for (const ex of exList) {
        if (muscleExercises.length >= MAX_PER_MUSCLE) break
        if (!seen.has(ex.id)) {
          seen.add(ex.id)
          muscleExercises.push(ex)
        }
      }
    }

    // 2. Complementar hasta MIN_PER_MUSCLE con otros equipos disponibles en la BD
    if (muscleExercises.length < MIN_PER_MUSCLE) {
      for (const eq of SUPPLEMENT_EQUIPMENT) {
        if (muscleExercises.length >= MIN_PER_MUSCLE) break
        if (equipment.includes(eq)) continue // ya procesado
        const exList = muscleData[eq]
        if (!exList) continue
        for (const ex of exList) {
          if (muscleExercises.length >= MIN_PER_MUSCLE) break
          if (!seen.has(ex.id)) {
            seen.add(ex.id)
            muscleExercises.push({ ...ex, supplement: true })
          }
        }
      }
    }

    result.push(...muscleExercises)
  }

  return result
}
